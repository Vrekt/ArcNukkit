package arc.check.permission;

/**
 * A basic permissions utility
 * TODO: May be an enum later.
 */
public final class Permissions {

    /**
     * Represents a permission that will exempt the player from all checks.
     */
    public static final String PERMISSION_EXEMPT = "arc.exempt";

    /**
     * Represents a permission that will allow players to see violations.
     */
    public static final String PERMISSION_NOTIFY = "arc.notify";

}
